//Constructing Strings with Variables
const myName = "Gloryson";
const joinStr = "Hello," + myName + " how are you?";
console.log(joinStr);


//Concatenating Strings with the Plus Equals Operator
let myStr = "My name is ";
myStr += "Gloryson.";
console.log(myStr);


//Appending Variables to Strings
let ourStr = "My India is ";
yourStr = "great. ";
ourStr += yourStr;
console.log(ourStr);